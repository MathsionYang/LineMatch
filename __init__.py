'''
opencv可以检测图像的主要特征,然后提取这些特征,使其成为图像描述符.
可以检测图像的主要特征,并通过单应性(homography)来检测图像是否存在其他的图像中.
常用的特征检测和提取算法有:
1.Harris:该算法用于检测角点
2.SIFI:该算法用于检测斑点
3.SURF:该算法用于检测斑点(blob)
4.FAST:该算法用于检测角点
5.BRIEF:该算法用于检测斑点
6.ORB:该算法代表带方向的FAST算法与具有旋转不变性的BRIEF算法
通过以下方法进行匹配:
1.暴力(Brute-Force)匹配法
2.基于FLANN的匹配法
可以采用单应性进行空间验证
'''
#特征是有意义的图像区域,该区域具有独特性或易与识别性
#因此,角点及高密布区域,边缘和斑点以及脊向(细长物体的对称轴)可以认为是很好的特征
